/* The first class we have programmed in EECS */
public class MyFirstClass extends Object {
  
  /* some fields */
  public double factor;
  
  /* a method to compute the subtraction of two integers */
  public int subtract(int x, int y) {
    return x - y;
  }
  
  /* multiply a value by the factor*/
  public double scale(double x) {
    return x * factor;
  }
}