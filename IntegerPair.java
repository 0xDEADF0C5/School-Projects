/** 
  * Creates integer pairs that print out stored values.
  * 
  * @author Erik Neu
  *
  * */
public class IntegerPair extends TwinNumbers{
  
  /**
   * Constructor sets the values for the integer pair.
   * @param a sets the value for the first value, value1
   * @param b sets the value for the second value, value2
   * */
  public IntegerPair(int a, int b) {
    super();
    this.setValue1(a);
    this.setValue2(b);
  }
}